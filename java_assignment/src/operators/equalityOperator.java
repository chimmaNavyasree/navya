package operators;

public class equalityOperator {

	public static void main(String[] args) {
		int a = 4;
        int b = 4;
        // Comparing a and b using == operator
        System.out.println("Are " + a + " and " + b + " equal? "+ (a == b));
        // Comparing a and b using != operator
        System.out.println("Are " + a + " and " + b + " equal? "+ (a != b));
	}

}
