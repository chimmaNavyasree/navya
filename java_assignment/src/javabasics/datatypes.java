package javabasics;

public class datatypes {

	public static void main(String[] args) {
		int num = 15;
		System.out.println(num);//integer
		boolean Bool = true;
		System.out.println(Bool);//boolean
		char Letter = 'N';
		System.out.println(Letter);//char
		float FloatNum = 5.99f;
		System.out.println(FloatNum);//float
		double d = 12.3;
		System.out.println(d);//double
	}

}
